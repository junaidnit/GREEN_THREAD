// Generates the extension's toolbar/store icons from the HERITAGE MARK ITSELF —
// the same traced path the site renders (src/components/brand-path.ts), not a
// hand-drawn approximation of it. The previous version redrew the ribbon with
// hand-written beziers and produced a shape that wasn't the logo.
//
// The mark is 1024x1389 (portrait). It is scaled to fit and CENTRED in the
// square canvas with its aspect ratio preserved — never stretched to fill.
// Usage: node scripts/render-ext-icons.mjs
import sharp from "sharp";
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";

const SIZES = [16, 32, 48, 128];
const here = (p) => new URL(p, import.meta.url).pathname.replace(/^\/([A-Za-z]):/, "$1:");
const OUT_DIR = here("../extension/icons/");

// pull the traced path straight from the component's source — one source of
// truth, so a re-trace of the artwork flows through to the icons
const src = readFileSync(here("../src/components/brand-path.ts"), "utf8");
const d = src.match(/d:\s*"([^"]+)"/)?.[1];
const viewBox = src.match(/viewBox:\s*"([^"]+)"/)?.[1];
if (!d || !viewBox) throw new Error("could not read BRAND_LOGO from brand-path.ts");
const [, , MARK_W, MARK_H] = viewBox.split(/\s+/).map(Number);

const CANVAS = "#F5F3EF"; // Fibre Set canvas
const INK = "#141414"; // the mark, as drawn in the original artwork

mkdirSync(OUT_DIR, { recursive: true });

for (const size of SIZES) {
  // fit the portrait mark inside the square with breathing room, preserving aspect
  const pad = size <= 32 ? 0.94 : 0.84; // small icons need proportionally more mark
  const scale = (size * pad) / MARK_H;
  const x = (size - MARK_W * scale) / 2;
  const y = (size - MARK_H * scale) / 2;

  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 ${size} ${size}">
    <rect width="${size}" height="${size}" rx="${size * 0.22}" fill="${CANVAS}"/>
    <g transform="translate(${x} ${y}) scale(${scale})"><path d="${d}" fill="${INK}"/></g>
  </svg>`;

  const png = await sharp(Buffer.from(svg)).png().toBuffer();
  writeFileSync(`${OUT_DIR}icon${size}.png`, png);
  console.log("wrote", `${OUT_DIR}icon${size}.png`);
}

// The content script needs the same geometry for its in-page badge, but it is
// plain JS and cannot import the TS module. Emit it as a content-script file
// so the badge, the icons and the website all draw one identical mark.
const markJs = `// AUTO-GENERATED by scripts/render-ext-icons.mjs — do not edit by hand.
// The heritage ribbon, traced from public/brand/logo-original.jpeg.
globalThis.FIBRESET_MARK = ${JSON.stringify({ d, viewBox, width: MARK_W, height: MARK_H }, null, 2)};
`;
writeFileSync(here("../extension/brand-mark.js"), markJs);
console.log("wrote extension/brand-mark.js");
